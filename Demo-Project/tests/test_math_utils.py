from math_utils import add, subtract

def test_add():
    result = add(2, 3)
    assert result == 5

def test_subtract():
    result = subtract(10, 3)
    assert result == 7

def test_add_negative():
    result = add(2, -3)
    assert result == -1

def test_subtract_negative():
    result = subtract(2, 3)
    assert result == -1